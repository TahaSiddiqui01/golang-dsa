package main

import "fmt"

func main() {
	fmt.Println("=================== Linked List ===================")

	// Linked List is a linear data structure that consists of a sequence of nodes.
	// Each node contains a value and a pointer to the next node.
	// The first node is called the head and the last node is called the tail.

	// There are two three types of linked list which is given below:
	// 1. Singly Linked List
	// 2. Doubly Linked List
	// 3. Circular Linked List

	// Singly Linked List:
	// A singly linked list is a linear data structure that consists of a sequence of nodes.
	// Each node contains a value and a pointer to the next node.

	// Doubly Linked List:
	// A doubly linked list is a linear data structure that consists of a sequence of nodes.
	// Each node contains a value and a pointer to the next node and a pointer to the previous node.

	// Circular Linked List:
	// A circular linked list is a linear data structure that consists of a sequence of nodes.
	// Each node contains a value and a pointer to the next node and a pointer to the previous node.
	// The last node points to the first node.

	// We can perform the following operations on the linked list:
	// 1. Insertion
	// 2. Deletion
	// 3. Search
	// 4. Update
	// 5. Traverse

	// Insertion:
	// - At the beginning
	// - At the end
	// - At a specific position

	// Deletion:
	// - At the beginning
	// - At the end
	// - At a specific position

	// Search:
	// - Linear Search
	// - Binary Search

	// Update:
	// - Update a node at a specific position

	// Traverse:
	// - Traverse the linked list

}
